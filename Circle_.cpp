#include <iostream>
using namespace std;
int main()
{
    int r;
    cout<<"Enter the radius of the circle: ";
    cin>>r;
    cout<<"The Diameter of the circle is "<<2*r<<endl;
    cout<<"The Circumference of the circle is "<<2*3.14*r<<endl;
    cout<<"The Area of the circle is "<<3.14*r*r<<endl;
    return 0;
}